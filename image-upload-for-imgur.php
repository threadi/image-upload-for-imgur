use ImgurImageUpload\Plugin\Init;
use ImgurImageUpload\Plugin\Installer;

// prevent direct access.
defined( 'ABSPATH' ) || exit;

// do nothing if PHP-version is not 8.0 or newer.
if ( version_compare( PHP_VERSION, '8.0', '<' ) ) {
	return;
}

// set constant with plugin path.
const IMAGE_UPLOAD_FOR_IMGUR_PLUGIN = __FILE__;

// set version.
const IMAGE_UPLOAD_FOR_IMGUR_PLUGIN_VERSION = '@@VersionNumber@@';

// get autoloader generated by composer.
require_once __DIR__ . '/vendor/autoload.php';

// initialize the plugin.
add_action(
	'plugins_loaded',
	function () {
		Init::get_instance()->init();
	}
);

/**
 * Run commands on activation of this plugin.
 */
register_activation_hook( IMAGE_UPLOAD_FOR_IMGUR_PLUGIN, array( Installer::get_instance(), 'activation' ) );
